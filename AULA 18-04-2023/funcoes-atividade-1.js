nome = "lucas"
idade = 18

function check(nome, idade) {
    if (idade >= 18){
        console.log(`${nome} sua idade é igual ou maior do que 18`)
    }else{
        console.log(`${nome} sua idade é menor do que 18`)
    }
}

/* CHAMAR A FUNÇÃO
Quando chamada, ela pega os valores do parametros abaixo, 
para assim realizar a função acima */
check(nome, 5) 

    

